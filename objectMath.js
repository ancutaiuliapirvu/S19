// Constanta PI
console.log(Math.PI);
// Ridicarea la putere
console.log(Math.pow(2, 3));
// Radicalul
console.log(Math.sqrt(25));
// Aproximari:
// Cel mai apropiat numar intreg mai mare decat numarul aproximat
console.log(Math.ceil(9.49));
// Cel mai apropiat numar intreg mai mic decat numarul aproximat
console.log(Math.floor(9.49));
// Proximarea ca in liceu, la calcului mediei
console.log(Math.round(9.49));
// Numar random, interval 0-1
console.log(Math.random());
// Numar random, interval 0-10
console.log(Math.random() * 10);
// Numar random, interval 1-10
console.log(Math.random() * 9 + 1);